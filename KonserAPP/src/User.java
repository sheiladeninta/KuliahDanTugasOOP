public class User {
    private String name;
    private String noHP;

    public User(String name, String noHP) {
        this.name = name;
        this.noHP = noHP;
    }

    public String getName() {
        return name;
    }

    public String getNoHP() {
        return noHP;
    }

    // Other methods for user attributes can remain the same.
}
